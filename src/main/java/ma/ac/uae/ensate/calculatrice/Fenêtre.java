    private void afficher(String nombre){
        if(LabelAffichage.getText().equals("0") || operationOn){
            nombreAffiché = nombre;
            operationOn = false;
        }
        else{
            nombreAffiché = LabelAffichage.getText()+nombre;
        }
            
        LabelAffichage.setText(nombreAffiché);
    }
      private boolean contientVirgule(String nombre){
          return nombre.contains(".");
      }
      
      private void operation(Operation op){
           nombre1 = Double.parseDouble(LabelAffichage.getText());
        operation = op;
        operationOn = true;
      }
      
      private String enleverVirgule(String nombre){
          if(Double.parseDouble(nombre)==Math.floor(Double.parseDouble(nombre)))
              nombre = nombre.substring(0, nombreAffiché.length()-2);
          return nombre;
}
