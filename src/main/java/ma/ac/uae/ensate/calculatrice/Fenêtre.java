    private void EgaleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EgaleActionPerformed
        if(on){
            nombre2 = Double.parseDouble(LabelAffichage.getText());
            if(operation==null)
                nombreAffiché = String.valueOf("Operation non reconnue");
            else if(operation==Operation.plus)
                nombreAffiché = String.valueOf(nombre1+nombre2);
            else if(operation==Operation.moins)
                nombreAffiché = String.valueOf(nombre1-nombre2);
            else if(operation==Operation.fois)
                nombreAffiché = String.valueOf(nombre1*nombre2);
            else
                if(nombre2!=0)
                    nombreAffiché = String.valueOf(nombre1/nombre2);
                else
                    nombreAffiché = String.valueOf("Division non définie");
        }
        LabelAffichage.setText(enleverVirgule(nombreAffiché));
    }//GEN-LAST:event_EgaleActionPerformed

